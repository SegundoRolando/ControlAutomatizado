public void Actualizar(){
        Cargo c=controladorCargo.Buscar(txtdat.getText());
            DefaultTableModel modelo=(DefaultTableModel) tblcargos.getModel();
            modelo.setRowCount(0);
           
            Object[] dato={
                c.getId(),
                c.getNombre()
            };
            modelo.addRow(dato);
    }
